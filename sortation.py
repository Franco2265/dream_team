# sortation.py

def main():
    # Creazione di una lista di numeri
    numeri = [5, 2, 9, 1, 5, 6]

    # Ordinamento della lista in modo crescente
    numeri_ordinat = sorted(numeri)

    # Stampa della lista ordinata
    print("Lista ordinata:", numeri_ordinat)

if __name__ == "__main__":
    main()